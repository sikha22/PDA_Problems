class Solution{
public:
    bool makePalindrome(int n,vector<string> &arr){
        // Code here
        unordered_set<string> st(arr.begin(), arr.end());

    int pairs = 0;
    
    for(string tmp : st) {
        reverse(begin(tmp), end(tmp));
        if(st.find(tmp) != st.end())
            ++pairs;
    }
    
    return pairs == n;
    }
};

